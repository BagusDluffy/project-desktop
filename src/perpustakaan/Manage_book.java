    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       try {
           String newpath = "src/upload";
           File directory = new File(newpath);
           if (!directory.exists()){
               directory.mkdirs();
           }
           File fileawal = null;
           File fileakhir = null;
           String ext = this.filename.substring(filename.lastIndexOf('.')+1);
           fileawal = new File(filename);
           fileakhir = new File(newpath+"/"+ID.getText()+"."+ext);
           System.out.println(fileakhir);
           book b = new book();
           this.stat = k.mycon().prepareStatement("insert into buku values(?,?,?,?,?,?)");
           stat.setString(1, b.id_buku);
           stat.setString(2, b.judul);
           stat.setString(3, b.kategori);
           stat.setString(4, b.penulis);
           stat.setString(5, b.terbit);
           stat.setString(6, fileakhir.toString());
           stat.executeUpdate();
           JOptionPane.showMessageDialog(rootPane, "Buku telah ditambahkan");
           Files.copy(fileawal.toPath(), fileakhir.toPath());
       } catch (Exception e){
           System.out.println();
       }
    }//GEN-LAST:event_btnSaveActionPerformed

