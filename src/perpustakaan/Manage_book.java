    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if ("".equals(ID.getText())||"".equals(Title.getText())||"".equals(Category.getText())||"".equals(Author.getText())||"".equals(Publication.getText())){
            JOptionPane.showMessageDialog(this, "Harap lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            String SQL =  "UPDATE buku SET "+
            "Judul='"+Title.getText()+"',Kategori='"+Category.getText()+"',Penulis='"+Author.getText()+"',Tahun_terbit='"+Publication.getText()+"' WHERE Id_buku="+ID.getText();
            int status = db.execute(SQL);
            if(status == 1){
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Data gagal diupdate", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int baris = table.getSelectedRow();
        if (baris != -1) {
            String ID = table.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM buku WHERE Id_buku='"+ID+"'";
            int status = db.execute(SQL);
            if (status==1) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih Dahulu", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

