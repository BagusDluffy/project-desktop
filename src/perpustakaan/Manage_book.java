    public void selectData(){
            String kolom[] = {"Id_buku","Judul","Kategori","Penulis","Tahun_terbit","Gambar"};
            DefaultTableModel dtm = new DefaultTableModel(null, kolom);
            String SQL = "SELECT * FROM buku";
            ResultSet rs = db.executeQuery(SQL);
            try {
                while(rs.next()) {
                    String Id_buku = rs.getString(1);
                    String judul = rs.getString(2);
                    String kategori = rs.getString(3);
                    String penulis = rs.getString(4);
                    String trbt = rs.getString(5);
                    String gmbr = rs.getString(6);
                    String data[] = {Id_buku,judul,kategori,penulis,trbt,gmbr};
                    dtm.addRow(data);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Admin_acount.class.getName()).log(Level.SEVERE, null, ex);
            }
            table.setModel(dtm);
    }
